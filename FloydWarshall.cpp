#include <bits/stdc++.h>

using namespace std;

#define INF 10000000
void printDistances(vector<vector<int>> &dist)
{
    cout << "Distances between every pair of vertexes: \n";
    int n = dist.size();
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (dist[i][j] == INF)
                cout << "INF ";
            else
                cout << dist[i][j] << " ";
        }
        cout << endl;
    }
}
void floydWarshall(vector<vector<int>> &dist)
{
    int n = dist.size();

    for (int k = 0; k < n; k++)
    {
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < n; j++)
            {
                if (dist[i][k] != INF && dist[k][j] != INF)
                {
                    dist[i][j] = min(dist[i][j], dist[i][k] + dist[k][j]);
                }
            }
        }
    }
    printDistances(dist);
}
int main()
{
    vector<vector<int>> dist = {
        {0, 4, INF, 5, INF},
        {INF, 0, 1, INF, 6},
        {2, INF, 0, 3, INF},
        {INF, INF, 1, 0, 2},
        {1, INF, INF, 4, 0}};
    floydWarshall(dist);
    printDistances(dist);
    return 0;
}