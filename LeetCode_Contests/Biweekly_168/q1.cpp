class Solution {
public:
    string lexSmallest(string s) {
        string r = s;
        for(int i = 1; i <= sz(s); i++){
            string tmp = s;
            reverse(tmp.begin(), tmp.begin() + i);
            string tmp2 = s;
            reverse(tmp2.begin(), tmp2.end());
            reverse(tmp2.begin(), tmp2.begin() + i);
            reverse(tmp2.begin(), tmp2.end());
            r = min({r, tmp, tmp2});
        }
        return r;
    }
};