class Solution {
public:
    
    string maxSumOfSquares(int num, int sum) {
        if(9*num < sum)
            return "";
        string res = "";
        while(sum >= 9){
            res += "9";
            sum -= 9;
            num--;
        }
        if(sum > 0){
            res += (sum + '0');
            num--;
        }
        if(num > 0){
            for(int i = 0; i < num; i++)
                res += "0";
        }
        return res;
            
    }
};